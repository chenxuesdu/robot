package com.robot.common;

public class RobotMenuId {
	int menuId;

	public RobotMenuId() {
	}

	public int getMenuId() {
		return menuId;
	}

	public void setMenuId(int menuId) {
		this.menuId = menuId;
	}

	@Override
	public String toString() {
		return "RobotMenuId{" + "menuId=" + this.menuId + "}";
	}

}
